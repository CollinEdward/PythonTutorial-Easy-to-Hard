# python dictionaries

# When we are making a dictionary, we give the item a name
x = {
  # fx here, 'brand' = ford  
  "brand": "Ford",
  "model": "Mustang",
  # we can also store intigers in the dictionary
  "year": 1964
}

# here we are printing the whole dictionary
print(x)

# We can also print only 1 item out of the dictionary
print(x["brand"]) # Here we call the dictionary and then the item name.

# just like a list, we can get the length

print(len(x))

# And get the type

print(type(x))